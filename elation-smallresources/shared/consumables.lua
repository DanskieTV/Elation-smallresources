-- Initialize categories
local Consumables = {
    alcohol = {},
    eat = {},
    drink = {},
    custom = {}
}

-- Helper function to add items to categories
local function addConsumable(name, data, category)
    if category then
        Consumables[category][name] = data.amount or 0
    else
        -- Categorize based on replenish type
        if data.alcohol and data.alcohol > 0 then
            Consumables.alcohol[name] = data.amount or 0
        elseif data.replenish == 'hunger' then
            Consumables.eat[name] = data.amount or 0
        elseif data.replenish == 'thirst' then
            Consumables.drink[name] = data.amount or 0
        else
            Consumables.custom[name] = data.amount or 0
        end
    end
end

